a = [2, 4, 1, 7, 9, 6]

# Output the second element: 4:
print("second element is {0}".format(a[1]))
# Output the second-to-last element: 9
print("second to last element is {0}".format(a[-2]))

# Output the last three elements in the array: [7, 9, 6]
print("last 3 elements are {0}".format(a[-3:]))

# Output the two middle elements in the array: [1, 7]
print("2 middle elements are {0}".format(a[2:4]))

# Output every element except the first one: [4, 1, 7, 9, 6]
print("all elements except the first one are {0}".format(a[1:]))

# Output every element except the last one: [2, 4, 1, 7, 9]
print("all elements except the last one are {0}".format(a[:len(a) - 1]))

